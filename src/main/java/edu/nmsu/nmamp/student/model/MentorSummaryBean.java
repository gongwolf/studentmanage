package edu.nmsu.nmamp.student.model;



public class MentorSummaryBean {
	private int mentor_id;
	private String name;
	private String email;
	private String deportment;
	private String intitution;
	
	
	public int getMentor_id() {
		return mentor_id;
	}
	public void setMentor_id(int mentor_id) {
		this.mentor_id = mentor_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getDeportment() {
		return deportment;
	}
	public void setDeportment(String deportment) {
		this.deportment = deportment;
	}
	
	public String getIntitution() {
		return intitution;
	}
	public void setIntitution(String intitution) {
		this.intitution = intitution;
	}
	



}
