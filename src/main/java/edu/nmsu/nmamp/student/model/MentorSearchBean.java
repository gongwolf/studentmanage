package edu.nmsu.nmamp.student.model;

public class MentorSearchBean {
	private String firstName;
	private String lastName;
	private String email;
	private String intitutionAbbr;
	private String intitutionFullName;
	private String department;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getIntitutionAbbr() {
		return intitutionAbbr;
	}
	public void setIntitutionAbbr(String intitutionAbbr) {
		this.intitutionAbbr = intitutionAbbr;
	}
	public String getIntitutionFullName() {
		return intitutionFullName;
	}
	public void setIntitutionFullName(String intitutionFullName) {
		this.intitutionFullName = intitutionFullName;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}

}
